`timescale 1ns/1ps

module spi_slave_tb;

//////////////////////////////////////////////////
// Parameters
//////////////////////////////////////////////////

parameter SPI_MODE = 0;

//////////////////////////////////////////////////
// DUT Signals
//////////////////////////////////////////////////

reg rst;
reg clk;

wire rx_dv;
wire [7:0] rx_byte;

reg tx_dv;
reg [7:0] tx_byte;

reg spi_clk;
reg mosi;
reg cs;
wire miso;

//////////////////////////////////////////////////
// Instantiate DUT
//////////////////////////////////////////////////

spi_slave #(SPI_MODE) DUT
(
    .rst(rst),
    .clk(clk),
    .rx_dv(rx_dv),
    .rx_byte(rx_byte),
    .tx_dv(tx_dv),
    .tx_byte(tx_byte),

    .spi_clk(spi_clk),
    .mosi(mosi),
    .cs(cs),
    .miso(miso)
);

//////////////////////////////////////////////////
// System Clock (FPGA clock)
//////////////////////////////////////////////////

always #5 clk = ~clk;   // 100 MHz

//////////////////////////////////////////////////
// SPI Clock
//////////////////////////////////////////////////

always #20 spi_clk = ~spi_clk;  // 25 MHz

//////////////////////////////////////////////////
// SPI Master Send Task
//////////////////////////////////////////////////

task spi_send_byte;
input [7:0] data;
integer i;
begin
    for(i = 7; i >= 0; i = i - 1)
    begin
        mosi = data[i];
        @(posedge spi_clk);
    end
end
endtask

//////////////////////////////////////////////////
// Test Sequence
//////////////////////////////////////////////////

initial begin

    // Initialize signals
    clk = 0;
    spi_clk = 0;
    rst = 0;
    cs = 1;
    mosi = 0;
    tx_dv = 0;
    tx_byte = 8'h00;

    #50;
    rst = 1;

    //////////////////////////////////////////////
    // Load TX byte to slave
    //////////////////////////////////////////////

    @(posedge clk);
    tx_byte = 8'h3C;
    tx_dv = 1;

    @(posedge clk);
    tx_dv = 0;

    //////////////////////////////////////////////
    // Start SPI transaction
    //////////////////////////////////////////////

    #50;
    cs = 0;

    spi_send_byte(8'hA5);

    #40;
    cs = 1;

    //////////////////////////////////////////////
    // Wait for RX
    //////////////////////////////////////////////

    #200;

    $display("Received byte = %h", rx_byte);

    #100;
    $finish;

end

endmodule
